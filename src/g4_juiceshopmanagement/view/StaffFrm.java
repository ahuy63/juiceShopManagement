    private void btn_staff_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_staff_saveActionPerformed
        // TODO add your handling code here:
        StaffDAO staffDAO = new StaffDAO();
        AccountDAO accountDAO = new AccountDAO();
        String username = txt_staff_username.getText();
        String password = txt_staff_password.getText();
        String fullname = txt_staff_fullname.getText();
        String email = txt_staff_email.getText();
        String phone = txt_staff_phone.getText();
        String idcard = txt_staff_idCardNumber.getText();
        boolean isAdmin = chkb_staff_isAdmin.isSelected();
        if (typeAction) {
            if (username.length() == 0 || password.length() == 0 || email.length() == 0) {
                JOptionPane.showMessageDialog(this, "Please enter full info!!!");
            } else if (accountDAO.isExist_Username(username)) {
                JOptionPane.showMessageDialog(this, "Username is existed!!!");
            } else {
                Account account = new Account();
                account.setUsername(username);
                account.setPassword(password);
                account.setFullName(fullname);
                account.setEmail(email);
                account.setPhone(phone);
                account.setAvatar(account.getUsername() + ".jpg");

                Staff staff = new Staff();
                staff.setAccountID(accountDAO.add(account));
                staff.setIDCardNumber(idcard);
                staff.setIsAdmin(isAdmin);
                if (staffDAO.add(staff)) {
                    loadData();
                } else {
                    JOptionPane.showMessageDialog(this, "Add failed!!!");
                }
            }
            changeStatusButton(true);
        } else {
            if (username.length() == 0 || password.length() == 0 || email.length() == 0) {
                JOptionPane.showMessageDialog(this, "Please enter full info!!!");
            } else {
                Account account = new Account();
                account.setUsername(username);
                account.setPassword(password);
                account.setFullName(fullname);
                account.setEmail(email);
                account.setPhone(phone);
                account.setAvatar(account.getUsername() + ".jpg");
                account.setID(Integer.parseInt(txt_staff_accountID.getText()));
                if (accountDAO.update(account)) {
                    Staff staff = new Staff();
                    staff.setAccountID(account.getID());
                    staff.setIDCardNumber(idcard);
                    staff.setIsAdmin(isAdmin);
                    if (staffDAO.update(staff)) {
                        loadData();
                    } else {
                        JOptionPane.showMessageDialog(this, "Update failed!!!");
                    }
                }

            }
            changeStatusButton(true);
        }
        txt_staff_username.setText("");
        txt_staff_password.setText("");
        txt_staff_fullname.setText("");
        txt_staff_email.setText("");
        txt_staff_idCardNumber.setText("");
        txt_staff_phone.setText("");
        chkb_staff_isAdmin.setSelected(false);
    }//GEN-LAST:event_btn_staff_saveActionPerformed

