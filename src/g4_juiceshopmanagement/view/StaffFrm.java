    private void btn_staff_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_staff_deleteActionPerformed
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(this, "Confirm delete?", "Warning", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
            int accountID = Integer.parseInt(txt_staff_accountID.getText());
            StaffDAO staffDAO = new StaffDAO();
            AccountDAO accountDAO = new AccountDAO();
            if (staffDAO.delete(accountID)) {
                if (accountDAO.delete(accountID)) {
                    loadData();
                    resetForm();
                }                
            } else {
                JOptionPane.showMessageDialog(this, "Delete failed!!!");
            }
        }
    }//GEN-LAST:event_btn_staff_deleteActionPerformed

