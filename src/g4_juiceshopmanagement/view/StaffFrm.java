    private void loadData() {
        DefaultTableModel model = (DefaultTableModel) tbl_Staff.getModel();
        StaffDAO staffDAO = new StaffDAO();
        ArrayList<Staff> list = staffDAO.getAll();
        int count = model.getRowCount();
        while (count > 0) {
            model.removeRow(count - 1);
            count--;
        }
        for (Staff item : list) {
            Object[] row = {
                item.getAccountID(),
                item.getAccount().getUsername(),
                item.getAccount().getPassword(),
                item.getAccount().getFullName(),
                item.getAccount().getEmail(),
                item.getAccount().getPhone(),
                item.getIDCardNumber(),
                item.isIsAdmin(),
                item.getAccount().getAvatar()
            };
            model.addRow(row);
        }
    }

